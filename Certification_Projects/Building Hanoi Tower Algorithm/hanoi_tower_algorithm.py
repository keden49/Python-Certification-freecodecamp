'''
Hanoi Tower Algorithm using the start and end logic
'''

def hanoi_solver(n):

  rods = [list(range(1,n+1))[::-1],[],[]]#initialize rods and reverse the order of the disks
  initial_state = f"{rods[0]} {rods[1]} {rods[2]}"+"\n" #saving the current state of the rods

  '''
  defining algorithm to solve the game
  start refers to the current position of the disks
  end refers to desired relocation
  '''

  def algorithm(disks,start,end):
    nonlocal rods#refers to the main rods list, to not lose state
    '''
    setting base case, to stop recursion at lowest point
    every move done is perfomed from the base case
    '''

    if disks == 1:
      rods[end].append(rods[start].pop())#append top disk to desired location
      return f"{rods[0]} {rods[1]} {rods[2]}"#returns current state of rods in formatted form

    else:

      '''
      recursive game play
      before any disk makes a move, all disks on top of it must first make the move
      '''

      helper_rod = 3-(start + end)#intermediate rod, before a disk moves to its desired location
      top_disk_move = algorithm(disks -1,start,helper_rod)
      current_disk_move = algorithm(1,start,end)
      top_disk_finalizer = algorithm(disks - 1,helper_rod,end)
      return top_disk_move +"\n" + current_disk_move + "\n" + top_disk_finalizer


  return initial_state + algorithm(n,0,2)