def verify_card_number(card_number):

    card_number = card_number.replace('-','').replace(' ','')

    reverse_card_number = card_number[::-1]

    odd_digits = reverse_card_number[0::2]
    even_digits = reverse_card_number[1::2]
    sum_of_odd_digits = 0
    sum_of_even_digits = 0

    for i in odd_digits:
        sum_of_odd_digits += int(i)

    for n in even_digits:
        n = int(n) * 2

        if n >= 10:
            n = (n//10) + (n%10)

        sum_of_even_digits += n

    total = sum_of_odd_digits + sum_of_even_digits

    if total % 10 == 0:
        return 'VALID!'

    else:
        return 'INVALID!'