def dfs_n_queens(n): # N-Queens solver

    if n < 1:
      return [] # Return empty list for n < 1

    # Initialize chessboard
    chess_board = [["." for _ in range(n)] for _ in range(n)]

    # Track columns, positive diagonals, negative diagonals
    column = []
    pos_diag = []
    neg_diag = []
    results = [] # List to store solutions

    # Backtracking function
    def backtrack(row):

        # Access non-local variables
        nonlocal column,pos_diag,neg_diag,chess_board

        # Base case: All queens placed
        if row == n:
            solutions = [r.index('Q') for r in chess_board]
            results.append(solutions) # Add solution to results
            return

        # Queens placement
        for col in range(n):

            # Skip if position is attacked
            if col in column or (row + col) in pos_diag or (row-col) in neg_diag:
                continue

            # Place queen
            column.append(col)
            pos_diag.append(row + col)
            neg_diag.append(row - col)
            chess_board[row][col] = 'Q'

            # look into future with current position
            # only returns after meeting deadends
            backtrack(row + 1)

            # change position after looking into future
            column.remove(col)
            pos_diag.remove(row + col)
            neg_diag.remove(row - col)
            chess_board[row][col] = '.'
            # move queen to next column


    backtrack(0) # Start backtracking from row 0


    return results # Return all solutions