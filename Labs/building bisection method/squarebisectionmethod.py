def square_root_bisection(target,tolerance = 0.001,max_iter = 100):
    if target < 0:
        raise ValueError("Square root of negative number is not defined in real numbers")
    elif target in [1,0]:
        print(f"The square root of {target} is {target}")
        return target
    else:
        upper_bound = max(1,target)
        lower_bound = 0
        root = None

        for _ in range(max_iter):
            mid = (upper_bound+lower_bound)/2
            square_mid = mid**2

            if (upper_bound-lower_bound) < tolerance:
                root = mid
                break

            elif square_mid > target:
                upper_bound = mid

            else:
                lower_bound = mid

        if root is None:
          print(f"Failed to converge within {max_iter} iterations")
          return None

        else:
          print(f"The square root of {target} is approximately {root}")
          return root